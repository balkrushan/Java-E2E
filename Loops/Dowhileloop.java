
public class Dowhileloop {
	
	//Do while loop will execute at least once if condition will be false.
	
	
	
	
	public static void main(String[] args) {
		
		int i=1;      //initialize variable.
		
		do {
			
			System.out.println(i); // Statement
			i++;
			
		} while(i<=5);           // Condition
		
	}

}
