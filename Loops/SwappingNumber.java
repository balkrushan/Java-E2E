
public class SwappingNumber {
          
	
	
	
	public static void main(String[]arg) {
		
		
		
		 int a =10;
		 int b=20;
		
		 int temp;
		
		 System.out.println("before swapping Value:" + "a =" +a +","+ "b= "+ b);
		 
		 temp =a;
		a=b;
		b =temp;
		
		System.out.println("swapping Value:" + "a =" +a +","+ "b= "+ b);
		
	}
}
